# Calculadora de conversão de temperatura


print("**********Conversão de Temperatura")

celsius = float(input(" \nQual a Temperatura em graus Celsius? "))

fahrenheit =  (celsius * 9 / 5) + 32

kelvin = celsius + 273

print(f" \nA conversão da temperatura de Celsius para fahrenheit é {fahrenheit} ")
print(f" \nA conversão da temperatura de Celsius para Kelvin é {kelvin}")